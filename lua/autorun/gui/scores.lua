local lg = love.graphics
local lm = love.mouse
local PANEL = {}

function PANEL:init()
	local w, h = love.graphics.getDimensions()
	self:setSize( w, h )
end 

function PANEL:paint()
	lg.setColor( 11, 11, 11, 120 )
	lg.rectangle( "fill", 0, 0, self:getWide(), self:getTall() )
end
gui.register( "scores", PANEL, "base" )